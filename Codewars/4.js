function getLength(arr) {
  let array = arr.length;
  return array;
}
function getFirst(arr) {
  let array = arr[0]
  return array;
}
function getLast(arr) {
  let array = arr[arr.length - 1]
  return array;
}
function pushElement(arr) {
  var el = 1;
  arr.push(el);
  return arr;
}
function popElement(arr) {
  arr.pop();
  return arr;
}

